//1: Variable datatype is determined at runtime by value assigned.
//2: 3 Primitive Types - string, number, boolean.
//3: Other Datatypes - undefined, object
//4: null is object datatype


let a = 10;
let b = true;
let c = "Str"
let d;
let e = [];
let f = {};
let g = null

console.log(typeof a); //number
console.log(typeof b); //boolean
console.log(typeof c); //string
console.log(typeof d); //undefined
console.log(typeof e); //object
console.log(typeof f); //object
console.log(typeof g); //object