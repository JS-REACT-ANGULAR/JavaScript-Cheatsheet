//Converting Strings to Numbers

//Number()	Returns a number, converted from its argument
//parseFloat()	Parses a string and returns a floating point number
//parseInt()	Parses a string and returns an integer

console.log(typeof Number("3.14"), Number("3.14"));
console.log(typeof Number(Math.PI), Number(Math.PI));
console.log(typeof Number(" "), Number(" "));
console.log(typeof Number(""), Number(""));
console.log(typeof Number("99 88"), Number("99 88"));
console.log(typeof Number("John"), Number("John"));

console.log(typeof parseFloat("10"), parseFloat("10"));
console.log(typeof parseInt("10"), parseInt("10"));